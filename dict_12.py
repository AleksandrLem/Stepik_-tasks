# m = int(input('количество поисковых запросов: '))
# name_phone = [input('введите имя: ').lower() for i in range(m)]


phone = [['79184219577', 'женя'], ['79194249271', 'руслан'], ['79281234567', 'женя']]
name_lst = ['руслан', 'женя', 'рустам']


# берем данные из списка phone и создаем словарь, где ключ - имя,
# а значение - список с номерами телефонов
phone_dict = {}
for num_name in phone:
    if num_name[1] not in phone_dict:
        phone_dict[num_name[1]] = [int(num_name[0])]
    else:
        phone_dict[num_name[1]] += [int(num_name[0])]

# выводим данные согласно условию задачи
for name in name_lst:
    answer = phone_dict.get(name)
    if answer == None:
        print('абонент не найден')
    else:
        print(*answer)


