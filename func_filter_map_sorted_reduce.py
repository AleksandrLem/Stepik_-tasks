# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –°—Ç–∞–≤–∏—Ç—å –∑–∞–ø—è—Ç—É—é –≤ –∫–æ–Ω—Ü–µ –≤—ã–≤–æ–¥–∞ –Ω–µ –Ω—É–∂–Ω–æ.

from functools import reduce

data = [['Tokyo', 35676000, 'primary'],
        ['New York', 19354922, 'nan'],
        ['Mexico City', 19028000, 'primary'],
        ['Mumbai', 18978000, 'admin'],
        ['Sao Paulo', 18845000, 'admin'],
        ['Delhi', 15926000, 'admin'],
        ['Shanghai', 14987000, 'admin'],
        ['Kolkata', 14787000, 'admin'],
        ['Los Angeles', 12815475, 'nan'],
        ['Dhaka', 12797394, 'primary'],
        ['Buenos Aires', 12795000, 'primary'],
        ['Karachi', 12130000, 'admin'],
        ['Cairo', 11893000, 'primary'],
        ['Rio de Janeiro', 11748000, 'admin'],
        ['Osaka', 11294000, 'admin'],
        ['Beijing', 11106000, 'primary'],
        ['Manila', 11100000, 'primary'],
        ['Moscow', 10452000, 'primary'],
        ['Istanbul', 10061000, 'admin'],
        ['Paris', 9904000, 'primary']]

# –ï—Å–ª–∏ –ø—Ä–æ–±–ª–µ–º—ã —Å –ø—Ä–∏–¥—É–º—ã–≤–∞–Ω–∏–µ–º –∫—É–¥–∞ –∑–∞—Å—É–Ω—É—Ç—å –≤—Å–µ —ç—Ç–∏ —Ñ—É–Ω–∫—Ü–∏–∏:

# filter - –¥–ª—è —Ç–æ–≥–æ —á—Ç–æ–±—ã –≤—ã–±—Ä–∞—Ç—å –Ω—É–∂–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å–ø–∏—Å–∫–∞
# map - —á—Ç–æ–±—ã –æ—Ç —ç–ª–µ–º–µ–Ω—Ç–∞ –æ—Å—Ç–∞–ª—Å—è —Ç–æ–ª—å–∫–æ –≥–æ—Ä–æ–¥
# sorted - –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å –≥–æ—Ä–æ–¥–∞
# reduce - —á—Ç–æ–±—ã —Å–æ–µ–¥–∏–Ω–∏—Ç—å –∏—Ö –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É

# —Ñ–∏–ª—å—Ç—Ä—É–µ–º –ø–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤—É –∂–∏—Ç–µ–ª–µ–π –∏ –ø–æ primary
data_filter = filter(lambda lst: lst[1] >
                     10_000_000 and lst[2] == 'primary', data)

# –¥–µ–ª–∞–µ–º —Å–ø–∏—Å–æ–∫ —Ç–æ–ª—å–∫–æ —Å –≥–æ—Ä–æ–¥–∞–º–∏
data_map = map(lambda lst: lst[0], data_filter)

# —Å–æ—Ä—Ç–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –≤ –∞–ª—Ñ–∞–≤–∏—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
data_sorted = sorted(data_map)

# –ø–µ—Ä–µ—á–∏—Å–ª—è–µ–º –≥–æ—Ä–æ–¥–∞ —á–µ—Ä–µ–∑ –∑–∞–ø—è—Ç—É—é
data_reduce = reduce(lambda city1, city2: city1+', '+city2, data_sorted)
print('Cities:', data_reduce)
